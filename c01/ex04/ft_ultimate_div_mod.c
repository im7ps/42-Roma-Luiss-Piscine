#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	temp;
	int	temp1;

	if (*b != 0)
	{
		temp = *a / *b;
		temp1 = *a % *b;
		*a = temp;
		*b = temp1;
	}
}
