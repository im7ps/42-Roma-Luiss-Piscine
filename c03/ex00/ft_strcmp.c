int	ft_strcmp(char	*s1, char	*s2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (s1[0] == '\0' && s2[0] != '\0')
		j = 0 - s2[0];
	if (s1[i] != '\0' && s2[i] == '\0')
		j = 0 - s1[0];
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			j = s1[i] - s2[i];
			break ;
		}
		i++;
	}
	return (j);
}
