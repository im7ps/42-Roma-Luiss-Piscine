int	ft_strncmp(char	*s1, char	*s2, unsigned	int n)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && n > 0)
	{
		if (s1[i] != s2[i])
		{
			j = s1[i] - s2[i];
			break ;
		}
		n--;
		i++;
	}
	if (s1[i] == '\0' && s2[i] != '\0')
		j = -s2[i];
	if (s1[i] != '\0' && s2[i] == '\0')
		j = s1[i];
	return (j);
}
